function solution(sizes) {
  const SIZES = sizes.map((size) => [...size].sort((a, b) => a - b))

  const maxX = Math.max(...SIZES.map(([x, y]) => x))
  const maxY = Math.max(...SIZES.map(([x, y]) => y))

  return maxX * maxY
}
