// 이렇게 한번의 루프로 끝낼수 있긴 함. 2ms정도 빨라짐.
function solution(sizes) {
  const SIZES = sizes.map((size) => [...size].sort((a, b) => a - b))

  let maxX
  let maxY

  maxX = maxY = 0

  SIZES.forEach(([x, y]) => {
    if (maxX <= x) maxX = x
    if (maxY <= y) maxY = y
  })

  return maxX * maxY
}

